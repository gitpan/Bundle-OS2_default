package Bundle::OS2_default4;

$VERSION = '1.00';

1;

=head1 NAME

Bundle::OS2_default4 - Modules to install last for OS/2 binary distribution

=head1 SYNOPSIS

  perl -MCPAN -e "install Bundle::OS2_default4"

  perl_ -MCPAN -e "install Bundle::OS2_default4"

=head1 CONTENTS

OS2::Focus		- Focus get/set

OS2::Attrib		- DOSish (system/archive) file attributes

OS2::WinObject		- Win* API (=primitive) for dealing with WPS objects

OS2::Proc		- Get list of processes and other info

SOM			- access to SOM and DSOM objects (WPS too)

=cut

